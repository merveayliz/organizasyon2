const preloader = document.querySelector("#preloader");
const menuToggle = document.querySelector("#menuToggle");
const menuClose = document.querySelector("#menuClose");
const mobileMenu = document.querySelector("#mobileMenu");
const overlay = document.querySelector("#overlay");
const langButtons = document.querySelectorAll(".lang-btn");
const revealItems = document.querySelectorAll(".section-reveal");
const accordionItems = document.querySelectorAll(".accordion__item");
const galleryCards = document.querySelectorAll(".gallery-card");
const lightbox = document.querySelector("#lightbox");
const lightboxImage = document.querySelector("#lightboxImage");
const lightboxClose = document.querySelector("#lightboxClose");
const lightboxPrev = document.querySelector("#lightboxPrev");
const lightboxNext = document.querySelector("#lightboxNext");
const quoteForm = document.querySelector("#quote");

// Aydınlık / Karanlık Mod Seçicileri
const themeToggle = document.querySelector("#themeToggle");
const themeToggleMobile = document.querySelector("#themeToggleMobile");

let currentImageIndex = 0;

// Sayfa Açılış Animasyonu Yönetimi
window.addEventListener("load", () => {
    setTimeout(() => {
        if (preloader) preloader.classList.add("is-hidden");
    }, 600);
});

// Aydınlık & Karanlık Mod Fonksiyonu (Local_Storage Destekli)
function initTheme() {
    const savedTheme = localStorage.getItem("siteTheme") || "light"; // Varsayılan aydınlık açılır
    document.documentElement.setAttribute("data-theme", savedTheme);
    
    const handleThemeSwitch = () => {
        const currentTheme = document.documentElement.getAttribute("data-theme");
        const newTheme = currentTheme === "dark" ? "light" : "dark";
        document.documentElement.setAttribute("data-theme", newTheme);
        localStorage.setItem("siteTheme", newTheme);
    };

    if (themeToggle) themeToggle.addEventListener("click", handleThemeSwitch);
    if (themeToggleMobile) themeToggleMobile.addEventListener("click", handleThemeSwitch);
}
initTheme();

// Mobil Menü Mantığı
function setMenuState(isOpen) {
    mobileMenu.classList.toggle("is-open", isOpen);
    overlay.classList.toggle("is-open", isOpen);
    document.body.classList.toggle("menu-open", isOpen);
    menuToggle.setAttribute("aria-expanded", String(isOpen));
    mobileMenu.setAttribute("aria-hidden", String(!isOpen));
}

menuToggle.addEventListener("click", () => setMenuState(true));
menuClose.addEventListener("click", () => setMenuState(false));
overlay.addEventListener("click", () => setMenuState(false));

mobileMenu.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => setMenuState(false));
});

// Bölümler Ekrana Girerken Yumuşakça Belirme Animasyonu
const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
        if (entry.isIntersecting) {
            entry.target.classList.add("is-visible");
            revealObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.12 });

revealItems.forEach((item) => revealObserver.observe(item));

// Sık Sorulan Sorular (S.S.S) Akordeon Yapısı
accordionItems.forEach((item) => {
    const trigger = item.querySelector(".accordion__trigger");
    if (trigger) {
        trigger.addEventListener("click", () => {
            const isOpen = item.classList.contains("active");
            accordionItems.forEach((otherItem) => otherItem.classList.remove("active"));
            if (!isOpen) {
                item.classList.add("active");
            }
        });
    }
});

// Lightbox Belirli Resme Gitme Yapısı
function showImageAtIndex(index) {
    if (index < 0) {
        currentImageIndex = galleryCards.length - 1;
    } else if (index >= galleryCards.length) {
        currentImageIndex = 0;
    } else {
        currentImageIndex = index;
    }
    const targetCard = galleryCards[currentImageIndex];
    if (targetCard) {
        lightboxImage.src = targetCard.dataset.src;
    }
}

// Galeri Kartı Tetikleyicileri
galleryCards.forEach((card, index) => {
    card.addEventListener("click", () => {
        currentImageIndex = index;
        showImageAtIndex(currentImageIndex);
        lightbox.classList.add("is-open");
        document.body.classList.add("lightbox-open");
        lightbox.setAttribute("aria-hidden", "false");
    });
});

function closeLightbox() {
    lightbox.classList.remove("is-open");
    document.body.classList.remove("lightbox-open");
    lightbox.setAttribute("aria-hidden", "true");
    lightboxImage.src = "";
}

// Lightbox İçindeki Navigasyon Buton İşlevleri
if (lightboxPrev) {
    lightboxPrev.addEventListener("click", (e) => {
        e.stopPropagation();
        showImageAtIndex(currentImageIndex - 1);
    });
}

if (lightboxNext) {
    lightboxNext.addEventListener("click", (e) => {
        e.stopPropagation();
        showImageAtIndex(currentImageIndex + 1);
    });
}

if (lightboxClose) lightboxClose.addEventListener("click", closeLightbox);

lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox || event.target.classList.contains('lightbox__wrapper')) {
        closeLightbox();
    }
});

// Klavye Kontrolleri
document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
        setMenuState(false);
        closeLightbox();
    } else if (lightbox.classList.contains("is-open")) {
        if (event.key === "ArrowRight") showImageAtIndex(currentImageIndex + 1);
        if (event.key === "ArrowLeft") showImageAtIndex(currentImageIndex - 1);
    }
});

// Dil Yönetimi Fonksiyonu
function setLanguage(lang) {
    const dictionary = translations[lang];
    document.documentElement.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.dataset.i18n;
        if (dictionary[key]) {
            element.textContent = dictionary[key];
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
        const key = element.dataset.i18nPlaceholder;
        if (dictionary[key]) {
            element.placeholder = dictionary[key];
        }
    });

    langButtons.forEach((button) => {
        button.classList.toggle("active", button.dataset.lang === lang);
    });

    localStorage.setItem("siteLanguage", lang);
}

langButtons.forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

setLanguage(localStorage.getItem("siteLanguage") || "tr");

